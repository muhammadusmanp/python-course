# we use \n go to next line
print(" i am usman\n i live in pakistan\n i am 26")

# we use \t for tab
print("hello how are you\n \t hope you will be fine")

#  we use \\ for backslash
print("this is \\ usman")

# \" use this for double quotes
print("hello \" world")
# /' use this for single quotes
print("this is \' usman")